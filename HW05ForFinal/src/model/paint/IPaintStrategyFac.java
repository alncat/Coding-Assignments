package model.paint;

/**
 * An interface that defines a factory that instantiates a specific
 * IPaintStrategy
 * 
 * @author lss4, yh20, jfe2
 *
 */
public interface IPaintStrategyFac {
	/**
	 * Instantiate the specific IPaintStrategy for which this factory is
	 * defined.
	 * 
	 * @returns An IPaintStrategy instance.
	 */
	public IPaintStrategy make();
}
